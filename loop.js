/*
--------- 3 kinds of loop--------------
***while
***do while
***for
*/
/*-------while-------*/
let x = 0;
while (x < 10) {
  console.log(x);

  x++;
}
console.log("------------ends---------------");
/*----------
---------do while
---------*/
let y = 0;
do y++;
while (y < 10);
{
  console.log(y);
}
console.log("------------ends---------------");
/*----------------
----------------for
-----------------*/
for (i = 0; i < 10; i++) {
  console.log(i);
}
console.log("------------ends---------------");
